def _cmd_replay(args: argparse.Namespace) -> None:
    """Handle the ``replay`` subcommand."""
    from flux.vm.interpreter import Interpreter, VMError

    with open(args.input, "rb") as f:
        raw = f.read()

    # Strip FLUX header and extract just the code section
    bytecode = _extract_code_section(raw)

    vm = Interpreter(bytecode, max_cycles=args.cycles)

    # Save reference to the real _step method before monkey-patching
    original_step = vm._step

    def _tracing_step():
        original_step()
        pc = vm.pc
        # Decode the instruction at the *previous* PC for display
        if pc > 0 and pc <= len(bytecode):
            opcode_byte = bytecode[pc - 1]
            print(f"  [PC={pc:04x}] opcode=0x{opcode_byte:02X}")
        else:
            print(f"  [PC={pc:04x}] ???")
        if args.verbose and hasattr(vm, "regs"):
            regs = vm.regs
            reg_dump = ", ".join(
                f"R{i}={regs.read_gp(i)}"
                for i in range(min(8, 8))
            )
            print(f"           regs: {reg_dump}")

    vm._step = _tracing_step

    print(f"Replaying {args.input} (max {args.cycles} cycles) ...")
    try:
        cycles = vm.execute()
    except (IndexError, VMError) as exc:
        print(f"\n  Execution error at cycle {vm.cycle_count}: {exc}", file=sys.stderr)
        print(f"  The bytecode may use features not yet supported by the VM.", file=sys.stderr)
        sys.exit(1)
    print(f"Replay finished in {cycles} cycles. R0={vm.regs.read_gp(0)}")


