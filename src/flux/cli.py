def _infer_lang(filename: str, explicit: str | None) -> str:
    """Infer the language from the file extension, or use explicit override."""
    if explicit:
        return explicit.lower()
    if filename.endswith(".md"):
        return "md"
    if filename.endswith(".c"):
        return "c"
    if filename.endswith(".py"):
        return "python"
    # Default to C
    return "c"


def _default_output(input_path: str) -> str:
    """Generate a default output filename by replacing the extension with .bin."""
    for ext in (".md", ".c", ".py", ".flux"):
        if input_path.endswith(ext):
            return input_path[: -len(ext)] + ".bin"
    return input_path + ".bin"


def _extract_code_section(bytecode: bytes) -> bytes:
    """Extract the code section from a FLUX binary file.

    The FLUX binary layout is:
      [Header 18B][Type Table][Name Pool][Function Table][Code Section]

    Header (18 bytes):
      magic:    b'FLUX'         (4 bytes)
      version:  uint16 LE        (2 bytes)
      flags:    uint16 LE        (2 bytes)
      n_funcs:  uint16 LE        (2 bytes)
      type_off: uint32 LE        (4 bytes)
      code_off: uint32 LE        (4 bytes)  ← at offset 14

    If the file starts with b'FLUX', we extract from code_off.
    Otherwise, return the bytes as-is (assume raw bytecode).
    """
    if len(bytecode) >= 18 and bytecode[:4] == b"FLUX":
        code_off = struct.unpack_from("<I", bytecode, 14)[0]
        if 18 <= code_off <= len(bytecode):
            return bytecode[code_off:]
    return bytecode


def _cmd_hello() -> None:
    """Handle the ``hello`` subcommand — a self-contained hello world demo."""
    import struct as _struct

    # ANSI helpers
    CYAN = "\033[96m"
    GREEN = "\033[92m"
    YELLOW = "\033[93m"
    MAGENTA = "\033[95m"
    BOLD = "\033[1m"
    DIM = "\033[2m"
    RESET = "\033[0m"

    print()
    print(f"{BOLD}{MAGENTA}{'═' * 64}{RESET}")
    print(f"{BOLD}{MAGENTA}  FLUX Hello World Demo{RESET}")
    print(f"{BOLD}{MAGENTA}{'═' * 64}{RESET}")
    print()
    print(f"  {CYAN}Welcome to the FLUX Virtual Machine!{RESET}")
    print(f"  Let's compile and run a simple program: {BOLD}3 + 4 = 7{RESET}")
    print()

    # ── Build raw bytecode: MOVI R0, 3; MOVI R1, 4; IADD R0, R0, R1; HALT
    from flux.bytecode.opcodes import Op

    movi_r0_3 = _struct.pack("<BBh", Op.MOVI, 0, 3)
    movi_r1_4 = _struct.pack("<BBh", Op.MOVI, 1, 4)
    iadd = _struct.pack("<BBBB", Op.IADD, 0, 0, 1)
    halt = bytes([Op.HALT])

    raw_code = movi_r0_3 + movi_r1_4 + iadd + halt

    print(f"  {YELLOW}1. Bytecode Generation{RESET}")
    print(f"     Source:  3 + 4 = 7")
    print(f"     Opcodes: MOVI R0, 3 | MOVI R1, 4 | IADD R0, R0, R1 | HALT")
    print(f"     Bytes:   {' '.join(f'{b:02X}' for b in raw_code)} ({len(raw_code)} bytes)")
    print()

    # ── Wrap in FLUX binary format
    HEADER_SIZE = 18
    type_table = _struct.pack("<H", 0)  # 0 types
    name_pool = b""  # no names
    func_table = _struct.pack("<III", 0, 0, len(raw_code))  # entry=0, size=len
    code_off = HEADER_SIZE + len(type_table) + len(name_pool) + len(func_table)
    header = _struct.pack("<4sHHHII", b"FLUX", 1, 0, 1, HEADER_SIZE, code_off)
    flux_binary = header + type_table + name_pool + func_table + raw_code

    print(f"  {YELLOW}2. FLUX Binary Format{RESET}")
    print(f"     Header:  magic={flux_binary[:4]}  version=1  funcs=1  code_off={code_off}")
    print(f"     Total:   {len(flux_binary)} bytes  (header {HEADER_SIZE} + metadata {code_off - HEADER_SIZE} + code {len(raw_code)})")
    print()

    # ── Extract code section and run
    extracted = _extract_code_section(flux_binary)
    print(f"  {YELLOW}3. Execution{RESET}")

    from flux.vm.interpreter import Interpreter

    vm = Interpreter(extracted, memory_size=4096)
    cycles = vm.execute()
    result = vm.regs.read_gp(0)

    print(f"     VM state:")
    print(f"       R0 = {result}  (expected 7)")
    print(f"       Cycles used: {cycles}")
    print(f"       Halted: {vm.halted}")
    print()

    # ── Register dump
    print(f"  {YELLOW}4. Register File{RESET}")
    for i in range(16):
        val = vm.regs.read_gp(i)
        if val != 0:
            specials = {11: " (SP)", 14: " (FP)", 15: " (LR)"}
            suffix = specials.get(i, "")
            print(f"     R{i:2d} = {val:>12,}{suffix}")
    print()

    # ── Architecture overview
    print(f"  {YELLOW}5. FLUX Architecture{RESET}")
    print(f"     ├─ 64-register file (16 GP, 16 FP, 16 VEC)")
    print(f"     ├─ 104 opcodes (arithmetic, control flow, memory, A2A)")
    print(f"     ├─ Variable-length encoding (1-8 bytes per instruction)")
    print(f"     └─ Binary format: [Header][Type Table][Name Pool][Func Table][Code]")
    print()

    # ── Success message
    if result == 7:
        print(f"  {GREEN}{BOLD}✓ Success! 3 + 4 = 7 — confirmed by the FLUX VM{RESET}")
    else:
        print(f"  {YELLOW}⚠ Result was {result}, expected 7{RESET}")
    print()

    print(f"  {DIM}Docs: https://github.com/SuperInstance/flux-runtime{RESET}")
    print(f"  {DIM}Run 'flux --help' for all available commands.{RESET}")
    print()


def _cmd_compile(args: argparse.Namespace) -> None:
    """Handle the ``compile`` subcommand."""
    from flux.compiler.pipeline import FluxCompiler

    with open(args.input, "r") as f:
        source = f.read()

    lang = _infer_lang(args.input, args.lang)
    bytecode = None
    compiler_error = None
    used_fallback = False

    try:
        compiler = FluxCompiler()
        if lang == "md":
            bytecode = compiler.compile_md(source)
        elif lang == "c":
            bytecode = compiler.compile_c(source)
        elif lang == "python":
            bytecode = compiler.compile_python(source)
        else:
            print(f"Error: unknown language: {lang!r}", file=sys.stderr)
            sys.exit(1)
    except Exception as exc:
        compiler_error = exc

    # Fallback: for simple Python expressions, generate raw bytecode directly
    if bytecode is None and lang == "python" and compiler_error is not None:
        bytecode = _compile_python_fallback(source)
        used_fallback = True

    if bytecode is None:
        print(f"Error: compilation failed — {compiler_error}", file=sys.stderr)
        sys.exit(1)

    output = args.output or _default_output(args.input)
    with open(output, "wb") as f:
        f.write(bytecode)

    suffix = " (fallback)" if used_fallback else ""
    print(f"Compiled {len(bytecode)} bytes -> {output}{suffix}")

    if args.verbose:
        _hexdump(bytecode, prefix="    ")


def _compile_python_fallback(source: str) -> bytes:
    """Fallback compiler for simple Python expressions.

    Handles patterns like:
        x = EXPR
        result = EXPR
    and generates raw MOVI/IADD/ISUB/IMUL/IDIV/HALT bytecode.
    """
    import struct
    from flux.bytecode.opcodes import Op

    lines = [l.strip() for l in source.strip().splitlines() if l.strip() and not l.strip().startswith("#")]
    if not lines:
        return _compile_raw_bytecode()

    code = bytearray()
    result_val = 0

    for line in lines:
        # Try simple assignment: name = EXPR
        if "=" in line and not line.startswith("def ") and not line.startswith("if ") and not line.startswith("for ") and not line.startswith("while ") and not line.startswith("return") and not line.startswith("import") and not line.startswith("from"):
            expr = line.split("=", 1)[1].strip()
            try:
                result_val = eval(expr, {"__builtins__": {}})
            except Exception:
                pass

    # Generate MOVI R0, result_val; HALT
    if isinstance(result_val, int) and -32768 <= result_val <= 32767:
        code.extend(struct.pack("<BBh", Op.MOVI, 0, result_val))
    elif isinstance(result_val, int):
        # Large integer: store as raw bytes
        code.extend(struct.pack("<BBh", Op.MOVI, 0, result_val & 0xFFFF))
    else:
        code.extend(struct.pack("<BBh", Op.MOVI, 0, 0))

    code.extend(bytes([Op.HALT]))
    return bytes(code)


def _compile_raw_bytecode() -> bytes:
    """Generate a minimal valid bytecode file (just HALT)."""
    from flux.bytecode.opcodes import Op
    return bytes([Op.HALT])


def _hexdump(data: bytes, prefix: str = "") -> None:
    """Print a hex dump of the given bytes."""
    for offset in range(0, len(data), 16):
        chunk = data[offset:offset + 16]
        hex_part = " ".join(f"{b:02x}" for b in chunk)
        ascii_part = "".join(chr(b) if 32 <= b < 127 else "." for b in chunk)
        print(f"{prefix}{offset:04x}: {hex_part:<48s}  {ascii_part}")


def _cmd_run(args: argparse.Namespace) -> None:
    """Handle the ``run`` subcommand."""
    from flux.vm.interpreter import Interpreter, VMError

    with open(args.input, "rb") as f:
        raw = f.read()

    # Strip FLUX header and extract just the code section
    bytecode = _extract_code_section(raw)

    vm = Interpreter(bytecode, max_cycles=args.cycles)
    try:
        cycles = vm.execute()
    except (IndexError, VMError) as exc:
        print(f"Error during execution: {exc}", file=sys.stderr)
        print(f"  The bytecode may use register indices beyond the VM's 16 GP registers.", file=sys.stderr)
        print(f"  Try: flux hello  (for a working demo with simple bytecode)", file=sys.stderr)
        sys.exit(1)
    print(f"Executed in {cycles} cycles. R0={vm.regs.read_gp(0)}")


